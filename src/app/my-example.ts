@Component({
    selector: 'app-counter',
    template: `<div >
                <h1>Main FIle </h1><br>
                this is a value <h1>{{count}} {{ans}}</h1>
                <hr>
                <button class="btn btn-primary" (click)="increment()"><i class="fa fa-plus"></i> Increment</button>
                <button class="btn btn-danger" (click)="decrement()"><i class="fa fa-minus"></i> Decrement</button>
                </div>`,
    styles: [`h1{color:blue}
            div{height:700px;}`]
})
class CounterComponent implements OnInit {
    count: number;
    ans: string;
    constructor() {}
    ngOnInit() {
        this.count = 0;
    }
    increment(){
        this.count++;
        if (this.count >= 0){
          this.ans = '';
        }
    }
    decrement(){
        this.count--;
        if (this.count <= 0){
          this.ans = ' | Please use  increment';
          this.count = 0;
        }
    }
}



